package org.artemas.cs.dsalgo3;

public class LinearTimeExample {
    public void log(int[] numbers) {
        // O(n)
        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[i]);
        }

        // O(n)
        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[i]);
        }
    }
}
